package pack;

public class Armor {
    private final static int DAMAGE_REDUCTION = 3;
    private final static int OUTPUT_DAMAGES_REDUCTION = 1 ;
    public Armor (){
    }

    public int damageReduction(){
        return this.DAMAGE_REDUCTION;
    }


    public int outputDamagesReduction() {
        return this. OUTPUT_DAMAGES_REDUCTION;
    }
}
